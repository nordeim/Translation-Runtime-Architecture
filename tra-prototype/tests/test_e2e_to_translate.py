"""E2E test: full TRA pipeline on to_translate.md with a manual LLM hijack.

This is the canonical end-to-end regression test. It loads the real
`to_translate.md` source (a Chinese review of the TRA repo), hijacks the
`llm_translate` seam to return the pre-generated manual English translation
(`to_translate.en.md`), and runs the full Kernel pipeline at L3 and L4.

Assertions cover:
- Pipeline completes without ConformanceFailure at L3.
- Output matches the manual translation (the hijack works end-to-end).
- Audit trail has ≥6 records with the canonical ISA sequence
  (ANALYZE_DOCUMENT → BUILD_GLOSSARY → BUILD_ENTITY_TABLE →
  TRANSLATE_SEGMENT → VERIFY_OUTPUT → VERIFY_OUTPUT at L3+).
- Zero BLOCKING diagnostics at L3.
- All runtime artifacts are written (glossary, entity_table, structural_map,
  style_profile, execution_log, repair_history, audit_trace).
- L4 emits evidence_trace.jsonl + ambiguity_register.json (L4-only).
- Two runs produce byte-identical audit_trace.jsonl (TRA-013 reproducibility).

The manual translation (`to_translate.en.md`) is the "AI response" — it is
pre-generated by a human (or LLM-assisted) translator and returned by the
`llm_translate` callback. This simulates an AI-powered translation where
the TRA engine handles analysis, verification, repair, and audit while the
LLM provides the fluent phrasing.
"""

from __future__ import annotations

import hashlib
import json
from pathlib import Path

from tra.config import BootstrapConfig
from tra.kernel import TRAKernel
from tra.memory import ConformanceLevel

# ---------------------------------------------------------------------------
# Fixtures
# ---------------------------------------------------------------------------

REPO_ROOT = Path(__file__).resolve().parent.parent.parent
SOURCE_PATH = REPO_ROOT / "to_translate.md"
MANUAL_TRANSLATION_PATH = REPO_ROOT / "tra-prototype" / "to_translate.en.md"
CONFIG_PATH = REPO_ROOT / "tra-prototype" / "config.yaml"


def _load_source() -> str:
    """Load the Chinese source document."""
    return SOURCE_PATH.read_text(encoding="utf-8")


def _load_manual_translation() -> str:
    """Load the pre-generated manual English translation (the AI response)."""
    return MANUAL_TRANSLATION_PATH.read_text(encoding="utf-8")


def _make_cfg(tmp_path: Path, level: ConformanceLevel) -> BootstrapConfig:
    """Build a BootstrapConfig for the given conformance level, with all
    runtime artifacts isolated under tmp_path."""
    base = BootstrapConfig.from_yaml(str(CONFIG_PATH))
    return base.model_copy(
        update={
            "base_dir": str(tmp_path),
            "conformance_level": level,
            "audit_trace": str(tmp_path / "audit_trace.jsonl"),
            "compilation_dir": str(tmp_path / "compilation_artifacts"),
            "cache_directory": str(tmp_path / "cache"),
        }
    )


def _run_kernel_with_manual_llm(
    cfg: BootstrapConfig, source: str, manual_translation: str
) -> tuple[TRAKernel, str]:
    """Run the TRA kernel with the llm_translate seam hijacked to return
    the manual translation.

    Returns (kernel, target) so the caller can inspect the audit trail and
    artifacts.
    """
    call_count = 0

    def manual_llm(source_segment: str, ctx: object) -> str:
        nonlocal call_count
        call_count += 1
        return manual_translation

    # Patch translate_segment in the kernel's namespace so the kernel's
    # _execute_translation call picks up the llm_translate override.
    import tra.kernel as kernel_mod

    orig_translate = kernel_mod.translate_segment

    def patched_translate(source_segment, ctx, cache, evidence, audit, **kwargs):
        kwargs["llm_translate"] = manual_llm
        return orig_translate(source_segment, ctx, cache, evidence, audit, **kwargs)

    kernel_mod.translate_segment = patched_translate
    try:
        kernel = TRAKernel(cfg)
        target = kernel.run(source)
    finally:
        # Restore the original so other tests aren't affected.
        kernel_mod.translate_segment = orig_translate

    assert call_count >= 1, "llm_translate was never called — hijack failed"
    return kernel, target


